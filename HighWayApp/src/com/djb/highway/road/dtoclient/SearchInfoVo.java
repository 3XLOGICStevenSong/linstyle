package com.djb.highway.road.dtoclient;

public class SearchInfoVo {

	private String stationName;

	private String stationCode;

	private String stationPile;

	private String stationEventFlag;

	private String stationWeather;

	private String eventFlagL;

	private String AreaFlagL;

	private String shareFlagR;

	public SearchInfoVo() {
	}

	public String getStationName() {
		return stationName;
	}

	public void setStationName(String stationName) {
		this.stationName = stationName;
	}

	public String getStationCode() {
		return stationCode;
	}

	public void setStationCode(String stationCode) {
		this.stationCode = stationCode;
	}

	public String getStationEventFlag() {
		return stationEventFlag;
	}

	public void setStationEventFlag(String stationEventFlag) {
		this.stationEventFlag = stationEventFlag;
	}

	public String getStationWeather() {
		return stationWeather;
	}

	public void setStationWeather(String stationWeather) {
		this.stationWeather = stationWeather;
	}

	public String getEventFlagL() {
		return eventFlagL;
	}

	public void setEventFlagL(String eventFlagL) {
		this.eventFlagL = eventFlagL;
	}

	public String getShareFlagR() {
		return shareFlagR;
	}

	public void setShareFlagR(String shareFlagR) {
		this.shareFlagR = shareFlagR;
	}

	public String getStationPile() {
		return stationPile;
	}

	public void setStationPile(String stationPile) {
		this.stationPile = stationPile;
	}

	public String getAreaFlagL() {
		return AreaFlagL;
	}

	public void setAreaFlagL(String areaFlagL) {
		AreaFlagL = areaFlagL;
	}

}
