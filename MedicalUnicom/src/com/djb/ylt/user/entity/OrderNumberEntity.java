package com.djb.ylt.user.entity;

import com.djb.ylt.framework.entity.BaseEntity;

public class OrderNumberEntity extends BaseEntity {
    /**
     * 
     */
    private static final long serialVersionUID = 3025620247815642960L;
    private Long id;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public boolean equals(Object that) {
        if (this == that) {
            return true;
        }
        if (that == null) {
            return false;
        }
        if (getClass() != that.getClass()) {
            return false;
        }
        OrderNumberEntity other = (OrderNumberEntity) that;
        return (this.getId() == null ? other.getId() == null : this.getId().equals(other.getId()));
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((getId() == null) ? 0 : getId().hashCode());
        return result;
    }
}